# Exercise 1d: Git Strengths & Weaknesses

# Strengths:
# 1. Distributed Version Control: Every user has a full repository history, enabling offline work and redundancy.
# 2. Branching/Merging: Isolate features/fixes in branches and merge them seamlessly.

# Weaknesses:
# 1. Learning Curve: Complex commands (e.g., rebase, conflict resolution) challenge beginners.
# 2. Large File Handling: Struggles with binary files (e.g., .xlsx, .png) without Git LFS.